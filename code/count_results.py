def parse_input(raw):
    raw = raw.strip()
    if raw.startswith('{'):
        data = json.loads(raw)
        return {int(k): int(v) for k, v in data.items()}
    else:
        answers = [int(x) for x in re.split(r'\s+', raw) if x.isdigit()]
        return {i+1: ans for i, ans in enumerate(answers)}

def calculate_raw_score(factor_code, answers):
    score = 0
    key_rules = FACTOR_KEY[factor_code]

    for q_num, valid_letters in key_rules:
        if q_num not in answers:
            continue

        user_val = answers[q_num]
        letter_map = {0: 'a', 1: 'b', 2: 'c'}
        user_letter = letter_map[user_val]

        if user_letter in valid_letters:
            if factor_code == 'B':
                score += 1
            else:
                score += 2 if user_val in [0, 2] else 1
    return score

def get_sten(raw_score, factor_code):
    """Гарантированно возвращает стену от 1 до 10"""
    norms = NORM_TABLE[factor_code]

    # 1. Проверяем, попадает ли балл в таблицу
    for sten, (low, high) in norms.items():
        if low <= raw_score <= high:
            return sten

    # 2. Если балл ниже минимума таблицы — возвращаем 0
    min_bound = min(r[0] for r in norms.values())
    if raw_score < min_bound:
        return 0

    # 3. Если балл выше максимума таблицы — возвращаем 0
    max_bound = max(r[1] for r in norms.values())
    if raw_score > max_bound:
        return 0

    # На случай непредвиденных ошибок (техническая страховка)
    return 0

def get_interpretation_text(sten):
    if sten <= 3:
        return "Низкий уровень"
    elif sten >= 8:
        return "Высокий уровень"
    else:
        return "Средний уровень"

