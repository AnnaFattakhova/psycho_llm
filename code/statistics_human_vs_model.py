import matplotlib.pyplot as plt
import pandas as pd
import seaborn as sns

# ==========================================
# 1. DATA PREPARATION
# ==========================================
target_scales = ['epi_extraversion', 'epi_neuroticism']

df_traits = df[
    df['scale'].isin(target_scales)
].copy()

# ==========================================
# 2. AGGREGATION
# ==========================================
df_agg = (
    df_traits
    .groupby(['model', 'scale'], as_index=False)
    .agg({
        'mean_total': 'mean',
        'human_score': 'mean',
        'model_score': 'mean'
    })
)

# ==========================================
# 3. WIDE TABLES
# ==========================================
mean_total_wide = (
    df_agg
    .pivot(index='model', columns='scale', values='mean_total')
    .reset_index()
)

mean_total_wide['source'] = 'mean_total'

human_wide = (
    df_agg
    .pivot(index='model', columns='scale', values='human_score')
    .reset_index()
)

human_wide['source'] = 'human_score'

model_wide = (
    df_agg
    .pivot(index='model', columns='scale', values='model_score')
    .reset_index()
)

model_wide['source'] = 'model_score'

# ==========================================
# 4. COMBINE
# ==========================================
plot_df = pd.concat([
    mean_total_wide,
    human_wide,
    model_wide
])

plot_df = plot_df.dropna(
    subset=['epi_extraversion', 'epi_neuroticism']
)

# ==========================================
# 5. TEMPERAMENT CLASSIFICATION
# ==========================================
MIDPOINT = 12

def assign_temperament(row):

    ext = row['epi_extraversion']
    neuro = row['epi_neuroticism']

    # stable + extravert
    if ext >= MIDPOINT and neuro < MIDPOINT:
        return 'Sanguine'

    # stable + introvert
    elif ext < MIDPOINT and neuro < MIDPOINT:
        return 'Phlegmatic'

    # unstable + introvert
    elif ext < MIDPOINT and neuro >= MIDPOINT:
        return 'Melancholic'

    # unstable + extravert
    else:
        return 'Choleric'

plot_df['temperament'] = plot_df.apply(
    assign_temperament,
    axis=1
)

