results = []

for test in df["test"].unique():

    temp = df[df["test"] == test]

    n = len(temp)

    # если слишком мало данных — пропускаем
    if n < 3:
        continue

    r, p = pearsonr(temp["human_score"], temp["model_score"])

    # доверительный интервал считаем только если r не идеальная
    if abs(r) < 0.999:

        z = np.arctanh(r)
        se = 1 / np.sqrt(n - 3)

        lo = z - 1.96 * se
        hi = z + 1.96 * se

        ci_low = np.tanh(lo)
        ci_high = np.tanh(hi)

    else:

        ci_low = np.nan
        ci_high = np.nan

    results.append([
        test,
        n,
        r,
        p,
        ci_low,
        ci_high
    ])

