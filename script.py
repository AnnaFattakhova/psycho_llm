        score = 0
        key_rules = FACTOR_KEY[factor_code]

        for q_num, valid_letters in key_rules:
            if q_num not in answers:
                continue

            user_val = answers[q_num]
            letter_map = {0: 'a', 1: 'b', 2: 'c'}
            user_letter = letter_map[user_val]

            if user_letter in valid_letters:
                if factor_code == 'B':
                    score += 1
