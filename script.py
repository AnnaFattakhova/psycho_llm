def score_eysenck(response_json):
    extraversion_yes = [1, 3, 8, 10, 13, 17, 22, 25, 27, 39, 44, 46, 49, 53]
    extraversion_no  = [5, 15, 20, 29, 32, 34, 37, 41, 51]
    neuroticism = [2, 4, 7, 9, 11, 14, 16, 19, 21, 23, 26, 28, 31, 33, 35, 38, 40, 43, 45, 47, 50, 52, 55, 57]
    lie_yes = [6, 24, 36]
    lie_no  = [12, 18, 30, 42, 48, 54]

    extraversion_cat = [(0, 8, "introversion "),
                        (9, 14, "average zone "),
                        (15, 24, "extraversion")]

    neuroticism_cat = [(0, 8, "low neuroticism"),
                       (9, 15, "average level"),
                       (16, 24, "high neuroticism")]

    lie_cat = [(0, 3, "invalid result"),
               (4, 6, "valid result")]

    def get_temperament(extraversion_score, neuroticism_score):
        if extraversion_score <= 8:
            orientation = "introvert"
        elif extraversion_score >= 15:
            orientation = "extravert"
        else:
            return "ambivert"

        if neuroticism_score <= 8:
            stability = "stable"
        elif neuroticism_score >= 16:
            stability = "unstable"
        else:
            return "avarage neuroticism"

        if orientation == "extravert" and stability == "stable":
            return "sanguine"
        elif orientation == "extravert" and stability == "unstable":
            return "choleric"
        elif orientation == "introvert" and stability == "stable":
            return "phlegmatic"
        elif orientation == "introvert" and stability == "unstable":
            return "melancholic"

    def calculate_scale(answers, yes_list, no_list):
        score = sum(answers[q-1] for q in yes_list) + \
                sum(1 - answers[q-1] for q in no_list)
        return score

