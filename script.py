    for scale, qs in scales.items():
        score = calc(answers_list, qs)
        total_score += score
        result[scale] = round(float(score), 2)

    gsi = total_score / 90
    result["GSI"] = {
        "score": round(float(gsi), 2),
        "interpretation": interpret_gsi(gsi)
    }

    return result

