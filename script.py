def run_test(client, model_name, system_prompt, test_text, temperature): # МЕНЯЕМ ТЕМПЕРАТУРУ

    prompt = [
        {"role": "system", "content": system_prompt},
        {"role": "user", "content": test_text}
    ]

    response = client.chat.completions.create(
        model=MODEL_NAME,
        messages=prompt,
        temperature=TEMPERATURE # МЕНЯЕМ ТЕМПЕРАТУРУ
    )
    text = response.choices[0].message.content


    clean_text = re.sub(r"^```[\w]*\n|```$", "", text.strip(), flags=re.MULTILINE)


    try:
      parsed = json.loads(clean_text)

      if "Ответ" in parsed:
        parsed["answer"] = parsed.pop("Ответ")
        return parsed

    except json.JSONDecodeError:
      return {"raw_response": clean_text}

def get_explanation(client, model_name, test_text, answer_json, explanation_prompt):

    answer_str = json.dumps(answer_json, ensure_ascii=False, indent=2)

    messages = [
        {
            "role": "system",
            "content": "Ты — большая языковая модель, которая ранее сгенерировала ответы на психологический тест."
        },
        {
            "role": "user",
            "content": f"Вопросы теста:\n{test_text}"
        },
        {
            "role": "assistant",
            "content": f"Ответы:\n{answer_str}"
        },
        {
            "role": "user",
            "content": explanation_prompt
        }
    ]

    response = client.chat.completions.create(
        model=MODEL_NAME,
        messages=messages,
    )

    text = response.choices[0].message.content
    clean_text = re.sub(r"^```[\w]*\n|```$", "", text.strip(), flags=re.MULTILINE)

    try:
        return json.loads(clean_text)
    except json.JSONDecodeError:
        return {"explanation": clean_text}

