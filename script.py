def score_attachment(response_json):
    key = [
        "FA","S","A","DA","S","FA","DA","A","S","DA",
        "FA","A","A","S","FA","A","S","FA","DA","A",
        "S","FA","A","S","FA","DA","FA","S","DA","FA",
        "DA","S","A","A","S","FA","DA","A","FA","S",
        "FA","DA","FA","A","S","A","DA","FA","DA","A",
        "A","DA","S","DA","FA","DA","A","S","DA","A"
    ]

    type_names = {
        "FA": "fearful avoidant ",
        "DA": "dismissive avoidant",
        "S": "secure attached",
        "A": "anxious preoccupied"
    }

    def calculate_attachment(answers, key):
        scores = {"FA":0, "DA":0, "S":0, "A":0}
        for i, ans in enumerate(answers):
            if ans == 1:
                scores[key[i]] += 1
        return scores

    answers = response_json["answer"]
    answers = {int(k): int(v) for k, v in answers.items()}
    answers_list = [answers[i] for i in range(1, 61)]

    if len(answers_list) != 60:
        return {"error": f"There must be 60 answers entered {len(answers_list)}"}

    result = calculate_attachment(answers_list, key)

    output = {}
    for t, score in result.items():
        output[type_names[t]] = round(float(score), 2)

    max_score = max(result.values())
    winners = [t for t, score in result.items() if score == max_score]

    if len(winners) == 1:
        output["dominant"] = type_names[winners[0]]
    else:
        output["dominant"] = [type_names[w] for w in winners]

    return output

